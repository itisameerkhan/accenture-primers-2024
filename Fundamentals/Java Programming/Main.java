import java.util.*;

public class Main {
    public static void main(String[] args) {
        int[][] a = new int[5][3];
        for(int i=0;i<5;i++) {
            for(int j=0;j<3;j++) {
                System.out.print(a[i][j] + " ");
            }
            System.out.println();
        }
    }    
}
